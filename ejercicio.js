//Usando una variable contador y bucle que recorra el array, 
//determinar el tamaño del siguiente array. 



let array = ['azul', 'rojo', 'verde', 'amarillo', 'naranja', 'morado'];
let size = 0;

for (let i = 0; i < array.length; i++) {
    size++;
}

console.log("El tamaño del array es: " + size);


